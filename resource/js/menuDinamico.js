$(function () {
    $("#home_slide").mouseover(function () {
        document.getElementById('menuHome').style.background = "darkblue";
    });
});
$(function () {
    $("#home_slide").mouseleave(function () {
        document.getElementById('menuHome').style.background = "linear-gradient(to bottom, rgba(30,87,153,1) 0%,rgba(41,137,216,1) 50%,rgba(32,124,202,1) 51%,rgba(125,185,232,1) 100%)";
    });
});
